============================================
Android端发送命令与Arduino处理对照表
============================================

1. 方向控制命令 (均不改变LCD显示的表情)
---------------- 
FORWARD    - 前进       
BACKWARD   - 后退
LEFT       - 逆时针转
RIGHT      - 顺时针转
STOP       - 急停
FLEFT      - 前左转
FRIGHT     - 前右转
BLEFT      - 后左转
BRIGHT     - 后右转

2. 速度控制命令
----------------
SPEED:[0-100]  (示例: SPEED:75)
- 速度百分比值，0=停止，100=全速

3. 菜单命令 
----------------
MENU:Smile (只改变LCD显示的表情)
MENU:Frown (只改变LCD显示的表情)
MENU:Sleep (只改变LCD显示的表情)
MENU:Default (回到初始状态)
MENU:SakiSakiSaki (回到特殊状态)

============================================
注意事项
============================================
1. 所有命令以换行符(\n)结尾
2. 命令区分大小写
3. 建议添加串口调试输出
4. 未识别的命令应忽略或返回错误